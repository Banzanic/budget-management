package com.example.budgetmanagement.model;

public class ExpensesModel {
    private Integer groceries;
    private Integer rent;
    private Integer transportation;
    private Integer subscriptions;
    private Integer healthCare;
    private Integer entertainment;
    private Integer debt;

    public Integer getGroceries() {
        return groceries;
    }

    public void setGroceries(Integer groceries) {
        this.groceries = groceries;
    }

    public Integer getRent() {
        return rent;
    }

    public void setRent(Integer rent) {
        this.rent = rent;
    }

    public Integer getTransportation() {
        return transportation;
    }

    public void setTransportation(Integer transportation) {
        this.transportation = transportation;
    }

    public Integer getSubscriptions() {
        return subscriptions;
    }

    public void setSubscriptions(Integer subscriptions) {
        this.subscriptions = subscriptions;
    }

    public Integer getHealthCare() {
        return healthCare;
    }

    public void setHealthCare(Integer healthCare) {
        this.healthCare = healthCare;
    }

    public Integer getEntertainment() {
        return entertainment;
    }

    public void setEntertainment(Integer entertainment) {
        this.entertainment = entertainment;
    }

    public Integer getDebt() {
        return debt;
    }

    public void setDebt(Integer debt) {
        this.debt = debt;
    }
}
